# for loop in python
for i in range(1,10):
    print(i)

for i in range(15,20):
    print(i)

for i in range(30,20): # it will print nothing
    print(i)

for i in range(30,20,-1):
    print(i)

for i in range(40,50,3):
    print(i)

for i in range(60,70,-2): # it will print nothing
    print(i)

# while loop in python
j = 10
while j<=20:
    print(j)
    j+=1

